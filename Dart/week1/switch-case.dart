//Switch case - used to evaluate expressions and execute diffrent blocks of code based on matching cases
void main() {
  int day = 0;
  switch (day) {
    case 1:
      print("🌞 Monday: Let's code!");
      break;
    case 2:
      print("🚀 Tuesday: Keep coding!");
      break;
    case 3:
      print("🐪 Wednesday: Halfway through!");
      break;
    default:
      print("🎉 Ladies and getlement the weekend is here!");
  }
}
